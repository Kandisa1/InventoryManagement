public void serialnum()
{
    conn=MySqlConnect.ConnectDB();
    String Sql="SELECT sr_no FROM invoice";
    try{
        pst=conn.prepareStatement(Sql);
            rs=pst.executeQuery();
            rs.next();
            rs.getString("sr_no");
            if (rs.getString("sr_no") == null) {
                serial.setText("1");    
            }else{
                long id = Long.parseLong(rs.getString("sr_no").substring(2, rs.getString("sr_no").length()));
                id++;
                serial.setText(""+ String.format("%07d", id));
            }
    }catch(Exception e){
        e.printStackTrace();
    }
}
