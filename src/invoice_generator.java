    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        itemprice = Double.parseDouble(price.getText());
        itemquantity = Double.parseDouble(quantity.getText());
        tablequantity = Double.parseDouble(product_stock);
        
        if (itemquantity>tablequantity){
            JOptionPane.showMessageDialog(this, "Quantity entered is more than the available quantity in the inventory");
            return;
        }else{
        
        itemsubtotal = itemprice*itemquantity;
        str1=String.valueOf(String.format("%.2f", itemsubtotal));
        subtotal.setText(str1);
        
        try{
            String value1 = tablequantity-itemquantity+"";
            String sql="update inventory_page set stock='"+value1+"'where sku='"+product_sku+"'";
            pst=conn.prepareStatement(sql);
            pst.execute();
        }catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

